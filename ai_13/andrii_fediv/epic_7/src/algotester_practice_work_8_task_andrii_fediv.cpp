#include <iostream>
#include <cstdint>
using namespace std;

int main()
{
    uint64_t a, n;
    cin >> a >> n;
    while (n--)
    {
        int x, y;
        cin >> x >> y;
        for (int i = 0; i < 8; i++)
            if (i+1 != x)
            {
                uint64_t mask2 = 1ULL << (i * 8 + y - 1);
                a ^= mask2;
            }
        uint64_t mask1 = 255ULL << ((x-1) * 8);
        a ^= mask1;
    }
    cout << a;
    return 0;
}
